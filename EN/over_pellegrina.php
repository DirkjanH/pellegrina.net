  <h2 class="begin"><em>La Pellegrina</em></h2>
  <p>La Pellegrina was founded in 1988 by dancer Doroth&eacute;e Wortelboer and musician Dirkjan Horringa for the organization of summer schools abroad. In the first years of <em>La Pellegrina </em>the courses were held in Italy, with historical dance and -music as subject. Since 1991 the courses have acquired a much wider scope and take place in Czechia and the Netherlands - this summer for the 35<sup>th</sup> time.</p>
  <h2>What does <em>La Pellegrina</em> mean? </h2>
  <p><em>La Pellegrina</em> (the Pilgrim Woman) is the name of a Italian play from the 16th century. When this play was to be performed at the marriage of Ferdinand I de' Medici, grand-duke of Tuscany, with Christine de Lorraine, granddaughter of the former queen-mother of France, they decided to make the play a little more lively. Between the play's five acts they added <em>intermedii</em>, musical interludes which were sung, played and danced. They also used spectacular machines which could make gods fly down from the sky and make boats float on stage. These <em>intermedii </em>made a huge impression, which eventually led to the birth of opera as an art form. The <em>Intermedii per La Pellegrina</em> from 1589 was in fact one of the first opera-like productions. Without it, <em>Orfeo</em> from Monteverdi would not have been imaginable.</p>
  <p>In 1988 Doroth&eacute;e Wortelboer and Dirkjan Horringa were invited to do a master class in Italy with the <em>Intermedii per La Pellegrina </em>as main subject. The inviting organization then went bankrupt. Doroth&eacute;e and Dirkjan decided overnight to organize the summer course themselves. To facilitate this they had to found an company and to choose a name for it; <em>La Pellegrina</em> was the obvious choice. Although in the beginning the focus was on the historical dance and opera, <em>La Pellegrina</em> nowadays doesn't focus on these subjects exclusively anymore, but covers a much wider scope of vocal and instrumental music from the renaissance until now.</p>
  <h2>All periods</h2>
  <p><em>La Pellegrina</em> focuses on music from the Baroque, the Classical period, the Romantic era and the 20th/21st century. The courses in Czechia focus on the rich Czech music culture. But there will also be attention for the German, Austrian, Hungarian and Slovakian composers, who belong after all to the same cultural area. For all the courses the century-old historic buildings in which will be rehearsed and where the concerts will take place create a unique atmosphere and lend extra inspiration to the musicians.</p>
