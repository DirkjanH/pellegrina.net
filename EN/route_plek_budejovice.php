<h2 class="begin">Travel information</h2>
<p> Perhaps you have a spare place in your car, or you want a ride. Perhaps you intend to travel by air, train or bus and prefer to travel with others. If you&nbsp;want to&nbsp;travel together&nbsp;by car or&nbsp;public transport&nbsp;you can look&nbsp;at&nbsp;the list of participants.&nbsp;It&nbsp;shows how&nbsp;one intends&nbsp;to travel.&nbsp;Please feel free to take up contact with other participants. Probably&nbsp;you will find&nbsp;like-minded&nbsp;souls and&nbsp;can&nbsp;agree on&nbsp;a  trip together. </p>
<h3>By  train or by air</h3>
<p>Those who travel by train, by bus or by air to Prague can best continue by train to &#268;esk&eacute; Bud&#283;jovice. We suggest the following bus and train connection: </p>
<img src="/Images/Locaties/Budejovice/IDOS.png" width="500" height="169">
<p>The above overview shows the connection from Prague airport to České Budějovice. The first leg is a direct bus from the airport to Prague main railway station (Praha hlavn&iacute; n&aacute;dra&#382;&iacute;). The second leg is a direct express train to &#268;esk&eacute; Bud&#283;jovice, arriving at 17:01. The total costs are about EUR 15. It is only a 500 meter walk from the railway station to the conservatoire. We will meet this train with a van to take large instruments and luggage. Other public transport connections can be found on <a href="https://idos.idnes.cz/en/vlakyautobusymhdvse/spojeni/" target="_blank">the Czech journey planner for all public transport</a> (also available in English and German).</p>
<p>An alternative option is travelling to Linz (Austria). From there, the train takes roughly two hours to České Budějovice. Linz can be reached by night train from the Netherlands and Germany, for example via this connection:</p>
<img src="/Images/Locaties/Budejovice/Utrecht-Linz.png" width="500" height="372">
<h3>By car </h3>
<p>Motorists coming from Western
	Europe are advised to use the following route: Frankfurt-N&uuml;rnberg-Amberg-border
	crossing Waidhaus/Rozvadov. Then keep following the motorway to Plzen until the exit for Nepomuk following the E49, keep going in the direction of &#268;esk&eacute; Bud&#283;jovice. From the Czech border it is a 200 km drive. Within Czechia you will need a vignette for the motorways. <a href="https://edalnice.cz/en/how-to-buy-an-electronic-vignette/index.html#/kiosk">You can buy it in advance via e-dalnice</a>, or at petrol stations at the border.</p>
 
