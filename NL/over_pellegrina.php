  <h2 class="begin"><em>La Pellegrina</em></h2>
  <p><em>La Pellegrina</em> werd in 1988 door danseres Dorothée Wortelboer
    en musicus Dirkjan Horringa opgericht om zomercursussen in het buitenland
    mogelijk te maken. De eerste jaren vonden deze cursussen in Italië plaats, met historische dans en -muziek als onderwerp. Vanaf 1991 vinden er jaarlijks cursussen plaats met een veel wijdere keus van muziek, in Tsjechië en Nederland - komende zomer dus voor de 36e keer.</p>
  <h2>Wat betekent <em>La Pellegrina</em>?</h2>
  <p><em>La Pellegrina </em>(de pelgrimse, vrouwelijke pelgrim dus) is de naam van een Italiaans toneelstuk uit de 16e eeuw. Toen dit toneelstuk bij een huwelijk in de Medici familie in 1589 in Florence zou worden opgevoerd, besloot men het te verlevendigen door tussen de vijf aktes van het toneelstuk bij wijze van intermezzi spektakels te spelen. Het was een serie spektakels waarin gezongen, gespeeld en gedanst werd en vernuftige toneelmachines voor in die tijd ongekende effecten zorgden, zoals goden die afdaalden uit de hemel op een wolk, grotten die zich openden en bootjes die rondvoeren op het toneel. Ze maakten zoveel indruk op tijdgenoten dat ze een trend zetten die tot de geboorte van de opera als kunstvorm leidde. De <em>Intermedii per La Pellegrina</em> uit 1589 waren in feite één van de eerste opera-achtige producties, zonder welke de <em>Orfeo</em> van Monteverdi ondenkbaar was geweest. </p>
  <p>In 1989 waren Dorothée Wortelboer en Dirkjan Horringa uitgenodigd om een zomercursus in Italië te geven met de <em>Intermedii per La Pellegrina</em> als onderwerp. De organisatie die hen had uitgenodigd en dit project liet plaatsvinden ging failliet. Daarop besloten Dorothée Wortelboer en Dirkjan Horringa het dan maar zelf te organiseren. Ze moesten daarvoor een organisatie oprichten; de naam <em>La Pellegrina</em> lag voor de hand... Hoewel het begin lag bij historische dans en opera, is dit allang niet meer het enige onderwerp waarmee <em>La Pellegrina</em> zich bezig houdt. </p>
  <h2>Van Barok tot nu</h2>
  <p> <i>La Pellegrina</i> richt zich op muziek uit de Barok, de Klassieke
    tijd, de Romantiek en de twintigste eeuw tot en met nu. In de cursussen in Tsjechië staat de rijke Tsjechische muziekcultuur centraal, met uitstapjes naar Duitse, Oostenrijkse, Slowaakse en Hongaarse
    componisten, die immers uit het zelfde cultuurgebied komen. Voor alle cursussen geldt, dat de eeuwenoude gebouwen waar repetities en concerten plaatsvinden voor een unieke sfeer en extra inspiratie zorgen. </p>
