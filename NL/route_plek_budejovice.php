<h2 class="begin">Reisinformatie</h2>
<p>Als je wilt samenreizen per auto of met het openbaar vervoer kunt
    je het beste naar de deelnemerslijst kijken en contact opnemen. Daar is aangegeven hoe men denkt te gaan reizen. Sommigen bieden plaatsen in een auto aan; anderen willen graag meerijden. Weer anderen gaan met het vliegtuig of de trein en reizen graag met anderen.  Waarschijnlijk vind je zo gelijkgestemde
zielen en kun je een gezamenlijke reis afspreken. </p>
<h3>Met de trein of het vliegtuig</h3>
<p>Wie met de trein, de bus  of het vliegtuig naar Praag reist, kan het beste met de trein verder reizen naar České Budějovice. Wij stellen de volgende bus- en treinverbinding voor: </p>
<img src="/Images/Locaties/Budejovice/IDOS.png" width="500" height="169">
<p>Bovenstaand overzicht geeft de verbinding van het vliegveld van Praag naar České Budějovice aan. Het eerste traject is een rechtstreekse bus van het vliegveld naar het hoofdstation van Praag (Praha hlavní nádraží). Het tweede traject is een rechtstreekse sneltrein naar České Budějovice, die om 17:01 uur aankomt. De totale kosten zijn ongeveer € 15. Het is slechts 500 meter lopen van het station naar het conservatorium. We wachten deze trein op met een busje om grote instrumenten en bagage mee te nemen. Andere verbindingen met het openbaar vervoer zijn te vinden op <a href="https://idos.idnes.cz/en/vlakyautobusymhdvse/spojeni/" target="_blank">de Tsjechische reisplanner voor al het openbaar vervoer</a> (ook beschikbaar in het Engels en Duits). </p>

<p>Een alternatieve mogelijkheid is reizen per trein naar Linz (Oostenrijk). Vandaar gaat een trein in iets meer dan twee uur naar České Budějovice. Linz is o.a. te bereiken met een nachttrein vanuit Nederland en Duitsland, via bijvoorbeeld deze verbinding:</p>
<img src="/Images/Locaties/Budejovice/Utrecht-Linz.png" width="500" height="372">
<h3>Met de auto </h3>
<p>Automobilisten die uit West-Europa komen, wordt geadviseerd de volgende route te gebruiken: Frankfurt-Nürnberg-Amberg grensovergang Waidhaus/Rozvadov. Blijf dan de snelweg naar Plzeň volgen tot afslag 76 naar Nepomuk en Písek over weg nr. 20 (E49). Rij vanaf hier over tweebaans wegen via Nepomuk naar Písek. Op de rondweg om Písek hou je richting Budějovice aan. Vanuit Utrecht is het 11 tot 12 uur rijden; een goed alternatief is het in twee rustige dagen te rijden. Binnen Tsjechië heb je een vignet nodig voor de snelwegen. Je kunt het van tevoren kopen via e-dalnice, of bij tankstations aan de grens.</p>
