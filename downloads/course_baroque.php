      <h3>Jean Gilles, <i>Messe des morts</i></h3>
      <p>Here is <a href="../pdf/2023-2/Gilles - Messe des Morts (ed. Vijftigschild)/Gilles Messe des morts score instr. dess 1, 2.pdf" target="_blank">a proposal for the instrumentation of dessus 1 and 2 of the Messe des Morts</a>: violins, oboes, flûtes allemande (traverso), and flûtes à bec (recorder).				We have tried to do justice to each instrument with interesting pieces.All indications are given in the score, but not in the parts, as it is better for people to apply them themselves.				</p>
      <p>In the 2nd dessus we tried as much as possible to stay within the range of the flute and oboe, but sometimes we did not quite succeed.<br>
        If the fragment is difficult to play, then the 1st dessus can be taken, if it is a few notes that are too low , then just leave them out, usually the 2nd violins play these notes as well.				The specific split fragments without violins are playable for everyone.</p>
      <p> In the Graduale the 4 flutes are divided into flûtes allemande (traverso) and flûtes à bec (recorder), 
        this to make the echoes stand out well.<br>
        In the vocal solos (especially the bass solos), where generally piano is indicated, we have reduced the number of instruments. Usually only the violins are playing.</p>
      <p>For the BC sections  in general holds: when only violins are playing, the bassoons stop playing.</p>
